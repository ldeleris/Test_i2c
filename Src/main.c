int main(void)
{

  /* USER CODE BEGIN 1 */


  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C3_Init();
  LED_Init();

  /* USER CODE BEGIN 2 */
  LCD_Init();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  int i = 0;
  int j = 0;

  while (1)
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
	  if (HAL_I2C_IsDeviceReady(&hi2c3, (uint16_t) address, 3, 1000) == HAL_OK)
	  {
		  LED_On();
		  LCD_dim(0);
		  //i = (~i) & 0x01;
		  if (j == 0)
		  {
			  LCD_display();
			  //LCD_startscrollright(0x00, 0x0F);
		  }
		  j++;
	  }
	  else
	  {
		  LED_Off();
	  }

	  if (j == 1)
	  {
		  LCD_clearDisplay();
		  //int y = 20;
		  for (int x=20;x<100;x++)
		  {
			  for (int y=20; y<40; y++)
			  {
				  LCD_drawPixel(x,y,WHITE);
			  }


		  }
		  i++;

		  j = 0;
	  }


	  HAL_Delay(20);


  }
  /* USER CODE END 3 */

}


